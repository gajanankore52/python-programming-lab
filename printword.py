#Accpet single digit no and print it into word

def main():
    
    iNo = int(input("Enter one number: "))
    
    if iNo == 1:
        print("One")  
        
    elif iNo == 2:
        print("Two")   
        
    elif iNo == 3:
        print("Three")
        
    elif iNo == 4:
        print("Four")
        
    elif iNo == 5:
        print("Five")
        
    elif iNo == 6:
        print("Six")
        
    elif iNo == 7:
        print('Seven')
        
    elif iNo == 8:
        print("Eight")
        
    elif iNo == 9:
        print("Nine")
        
    else:
        print("Invalid Number")
        

if __name__ == "__main__":
    main()